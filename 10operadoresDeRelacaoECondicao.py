# Operadores de igualdade 
# == , =! 


# Operadores relacionais

# > , < , <= < >= 

numero = 100 == 100 # um sinal de igual significa atribuição e dois sinais de igual significa comparação
print(numero)

numero = 100 != 100
print(numero)
# print(type(numero))

numero = 100 > 101
print(numero)

numero = 100 < 101
print(numero)

numero = 100 >= 101
print(numero)

numero = 100 <= 101
print(numero)
